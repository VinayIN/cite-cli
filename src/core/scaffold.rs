use std::fs;
use std::path::Path;
use tracing::{info, instrument};

use crate::core::CiteError;
use crate::core::manifest::Manifest;
use crate::core::metadata::Metadata;

const AUTO_GEN_HEADER: &str = r#"# This file was autogenerated by cite-cli init
# https://github.com/VinayIN/cite-cli
#
# Copyright 2026, Binay Pradhan
# SPDX-License-Identifier: MIT

"#;

pub struct InitReport {
    pub directories_created: Vec<String>,
    pub files_created: Vec<String>,
    pub files_skipped: Vec<String>,
}

#[instrument(skip(root), fields(project = %name))]
pub fn init_project(name: &str, root: &Path) -> Result<InitReport, CiteError> {
    let mut report = InitReport {
        directories_created: vec![],
        files_created: vec![],
        files_skipped: vec![],
    };

    if !root.exists() {
        fs::create_dir_all(root)?;
        let d = root.to_string_lossy().to_string();
        info!("Created directory: {d}");
        report.directories_created.push(d);
    }

    for sub in ["content", "assets/audio", "assets/image"] {
        let path = root.join(sub);
        if !path.exists() {
            fs::create_dir_all(&path)?;
            info!("Created directory: {sub}");
            report.directories_created.push(sub.to_string());
        }
    }

    let manifest_path = root.join("cite.toml");
    if !manifest_path.exists() {
        fs::write(&manifest_path, manifest_template(name)?)?;
        info!("Created file: cite.toml");
        report.files_created.push("cite.toml".to_string());
    } else {
        let msg = "cite.toml (already exists)".to_string();
        info!("Skipped: {msg}");
        report.files_skipped.push(msg);
    }

    let meta_path = root.join("metadata.yml");
    if !meta_path.exists() {
        fs::write(&meta_path, metadata_template()?)?;
        info!("Created file: metadata.yml");
        report.files_created.push("metadata.yml".to_string());
    } else {
        let msg = "metadata.yml (already exists)".to_string();
        info!("Skipped: {msg}");
        report.files_skipped.push(msg);
    }

    let gitignore_path = root.join(".gitignore");
    if !gitignore_path.exists() {
        fs::write(
            &gitignore_path,
            format!("{AUTO_GEN_HEADER}\nbuild/\n.cite-cache.json\n"),
        )?;
        info!("Created file: .gitignore");
        report.files_created.push(".gitignore".to_string());
    } else {
        let msg = ".gitignore (already exists)".to_string();
        info!("Skipped: {msg}");
        report.files_skipped.push(msg);
    }

    Ok(report)
}

fn manifest_template(name: &str) -> Result<String, CiteError> {
    let manifest = Manifest::default_template(name);
    let body = toml::to_string(&manifest).map_err(|e| CiteError::Config(e.to_string()))?;
    Ok(format!("{AUTO_GEN_HEADER}\n{body}"))
}

fn metadata_template() -> Result<String, CiteError> {
    let meta = Metadata::default();
    let body = serde_yaml::to_string(&meta)?;
    Ok(format!(
        "{AUTO_GEN_HEADER}# Add your podcast entries below. Each entry becomes one episode.\n\
#   title:     Display title of the episode\n\
#   file:      Path to the Markdown source\n\
#   source_url: Original URL (optional)\n\
#   category:  Category name (optional)\n\
#   thumbnail: Path under assets/image (optional)\n\
#   audio:     Path under assets/audio (optional)\n\
#   citation:  Path to BibTeX file (optional)\n\
{}\n",
        body.trim_end(),
    ))
}
